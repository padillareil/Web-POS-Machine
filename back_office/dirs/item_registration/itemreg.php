1. Basic Item Information (Required)

These are the minimum required fields to register an item:

Item Code / SKU (System-generated or manual)

Barcode (EAN-13 / UPC / custom)

Item Name

Description

Category

Brand

Unit of Measure (UOM) – e.g., PCS, BOX, KG

Status (Active / Inactive)

👉 These fields allow POS scanning and identification.

💰 2. Sales Information (POS Required)

Selling Price

Tax Type (VATable / Non-VAT / Zero-rated)

Discountable (Yes/No)

Price Level (Retail / Wholesale / VIP)

Allow Price Override (Yes/No)

Optional but recommended:

Cost + Markup %

Promo price

Bundle eligibility

📦 3. Inventory Control Fields

If you want stock monitoring:

Beginning Quantity

Reorder Level

Minimum Stock

Maximum Stock

Track Inventory (Yes/No toggle)

Serialized Item? (Yes/No)

Lot/Expiry Tracking? (Yes/No)

Warehouse / Branch Assignment

